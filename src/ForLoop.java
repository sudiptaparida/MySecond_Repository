public class ForLoop
{
    public static void main(String[] args)
    {
        int i;
        int j;
        for (i = 2; i <= 5; i++)
        {
            for (j = 3; j < i; j++)
            {
                System.out.print("*");

                System.out.println("Simply");
            }
        }
    }
}

